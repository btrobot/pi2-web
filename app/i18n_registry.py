def _validate_bootstrap_i18n() -> None:
    expected_locales = set(SUPPORTED_LOCALES)
    actual_locales = set(BOOTSTRAP_I18N)
    if actual_locales != expected_locales:
        raise ValueError(
            f"Bootstrap locales mismatch: expected {sorted(expected_locales)}, got {sorted(actual_locales)}"
        )

    expected_keys = set(BOOTSTRAP_I18N_KEYS)
    for locale, messages in BOOTSTRAP_I18N.items():
        actual_keys = set(messages)
        if actual_keys != expected_keys:
            raise ValueError(
                f"Bootstrap i18n keys mismatch for {locale}: "
                f"expected {sorted(expected_keys)}, got {sorted(actual_keys)}"
            )


_validate_bootstrap_i18n()


def get_bootstrap_i18n() -> dict[str, dict[str, str]]:
    """Return the frozen bilingual UI-label payload for bootstrap."""

    return {locale: dict(messages) for locale, messages in BOOTSTRAP_I18N.items()}
